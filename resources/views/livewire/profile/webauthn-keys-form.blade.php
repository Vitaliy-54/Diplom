// Функция инициализации WebAuthn
function initWebAuthn() {
    const registerBtn = document.getElementById('registerPasskeyBtn');
    
    // Удаляем старый обработчик, если есть
    if (registerBtn && registerBtn._webauthnHandler) {
        registerBtn.removeEventListener('click', registerBtn._webauthnHandler);
        delete registerBtn._webauthnHandler;
    }
    
    if (!registerBtn) return;
    
    // Создаем обработчик
    const handler = async () => {
        // --- АВТОМАТИЧЕСКОЕ ОПРЕДЕЛЕНИЕ ИМЕНИ УСТРОЙСТВА ---
        let suggestedDeviceName = "Мое устройство";
        
        try {
            // Способ 1: Современный API (Chrome, Edge, Brave)
            if (navigator.userAgentData && navigator.userAgentData.getHighEntropyValues) {
                const values = await navigator.userAgentData.getHighEntropyValues(["platform", "platformVersion", "model"]);
                const platform = values.platform;
                
                if (platform === "Android" && values.model) {
                    suggestedDeviceName = values.model;
                } else if (platform === "iOS") {
                    const ua = navigator.userAgent;
                    if (ua.includes('iPhone')) {
                        suggestedDeviceName = values.model || 'iPhone';
                    } else if (ua.includes('iPad')) {
                        suggestedDeviceName = values.model || 'iPad';
                    } else {
                        suggestedDeviceName = 'Apple Device';
                    }
                } else if (platform === "macOS") {
                    suggestedDeviceName = 'Mac';
                } else if (platform === "Windows") {
                    suggestedDeviceName = 'Windows PC';
                } else if (platform === "Linux") {
                    suggestedDeviceName = 'Linux PC';
                }
                
                if (platform === "Windows" && values.platformVersion) {
                    const version = parseInt(values.platformVersion);
                    if (version >= 10) suggestedDeviceName = 'Windows 10/11 PC';
                }
            } 
            else {
                const ua = navigator.userAgent;
                
                if (/iPhone/i.test(ua)) {
                    suggestedDeviceName = 'iPhone';
                    if (ua.match(/iPhone(\d+),(\d+)/)) {
                        const model = ua.match(/iPhone(\d+),(\d+)/);
                        if (model) suggestedDeviceName = `iPhone ${model[1]}`;
                    }
                } 
                else if (/iPad/i.test(ua)) {
                    suggestedDeviceName = 'iPad';
                    if (ua.match(/iPad(\d+),(\d+)/)) {
                        const model = ua.match(/iPad(\d+),(\d+)/);
                        if (model) suggestedDeviceName = `iPad ${model[1]}`;
                    }
                }
                else if (/Android/i.test(ua)) {
                    if (ua.includes('SM-')) suggestedDeviceName = 'Samsung Galaxy';
                    else if (ua.includes('Pixel')) suggestedDeviceName = 'Google Pixel';
                    else if (ua.includes('OnePlus')) suggestedDeviceName = 'OnePlus';
                    else if (ua.includes('Xiaomi')) suggestedDeviceName = 'Xiaomi';
                    else if (ua.includes('Huawei')) suggestedDeviceName = 'Huawei';
                    else suggestedDeviceName = 'Android';
                }
                else if (/Mac/i.test(ua)) {
                    suggestedDeviceName = 'Mac';
                }
                else if (/Windows/i.test(ua)) {
                    suggestedDeviceName = 'Windows PC';
                }
                else if (/Linux/i.test(ua)) {
                    suggestedDeviceName = 'Linux PC';
                }
            }
            
            if (suggestedDeviceName === 'Mac') {
                try {
                    const canvas = document.createElement('canvas');
                    const gl = canvas.getContext('webgl');
                    if (gl) {
                        const debugInfo = gl.getExtension('WEBGL_debug_renderer_info');
                        if (debugInfo) {
                            const renderer = gl.getParameter(debugInfo.UNMASKED_RENDERER_WEBGL);
                            if (renderer.includes('Apple M1')) suggestedDeviceName = 'MacBook M1';
                            else if (renderer.includes('Apple M2')) suggestedDeviceName = 'MacBook M2';
                            else if (renderer.includes('Apple M3')) suggestedDeviceName = 'MacBook M3';
                        }
                    }
                } catch(e) { console.debug('GPU detection failed:', e); }
            }
            
        } catch (err) {
            console.warn("Не удалось определить устройство:", err);
            const today = new Date();
            const dateStr = `${today.getDate().toString().padStart(2, '0')}.${(today.getMonth()+1).toString().padStart(2, '0')}.${today.getFullYear()}`;
            suggestedDeviceName = `Устройство (${dateStr})`;
        }
        
        // Диалог с предложением имени
        const finalDeviceName = prompt(
            `🔐 Привязка ключа безопасности\n\n` +
            `Мы определили ваше устройство как:\n"${suggestedDeviceName}"\n\n` +
            `Вы можете оставить это имя или ввести свое.`,
            suggestedDeviceName
        );
        
        if (!finalDeviceName) return;
        
        const csrfToken = document.querySelector('meta[name="csrf-token"]')?.content;
        
        try {
            if (!window.PublicKeyCredential) {
                throw new Error('Ваш браузер не поддерживает WebAuthn');
            }
            
            const isAvailable = await PublicKeyCredential.isUserVerifyingPlatformAuthenticatorAvailable();
            if (!isAvailable) {
                throw new Error('На устройстве не настроена биометрия');
            }
            
            const optionsResp = await fetch('/webauthn/register/options', {
                method: 'POST',
                headers: {
                    'X-CSRF-TOKEN': csrfToken,
                    'Accept': 'application/json',
                    'Content-Type': 'application/json'
                }
            });
            
            if (!optionsResp.ok) {
                throw new Error(`Сервер вернул ошибку ${optionsResp.status}`);
            }
            
            const options = await optionsResp.json();
            
            if (!options.publicKey) {
                throw new Error('Неверный формат ответа от сервера');
            }
            
            let challenge = options.publicKey.challenge.replace(/-/g, '+').replace(/_/g, '/');
            while (challenge.length % 4) challenge += '=';
            
            let userId = options.publicKey.user.id.replace(/-/g, '+').replace(/_/g, '/');
            while (userId.length % 4) userId += '=';
            
            const publicKey = {
                challenge: Uint8Array.from(atob(challenge), c => c.charCodeAt(0)),
                rp: options.publicKey.rp,
                user: {
                    id: Uint8Array.from(atob(userId), c => c.charCodeAt(0)),
                    name: options.publicKey.user.name,
                    displayName: options.publicKey.user.displayName
                },
                pubKeyCredParams: options.publicKey.pubKeyCredParams || [
                    {alg: -7, type: 'public-key'},
                    {alg: -257, type: 'public-key'}
                ],
                authenticatorSelection: options.publicKey.authenticatorSelection || {
                    authenticatorAttachment: 'platform',
                    residentKey: 'required',
                    userVerification: 'required'
                },
                attestation: options.publicKey.attestation || 'none',
                timeout: options.publicKey.timeout || 60000
            };
            
            const credential = await navigator.credentials.create({ publicKey });
            
            const registerResp = await fetch('/webauthn/register', {
                method: 'POST',
                headers: {
                    'X-CSRF-TOKEN': csrfToken,
                    'Accept': 'application/json',
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({
                    id: credential.id,
                    type: credential.type,
                    name: finalDeviceName,
                    response: {
                        attestationObject: arrayBufferToBase64Url(credential.response.attestationObject),
                        clientDataJSON: arrayBufferToBase64Url(credential.response.clientDataJSON),
                        transports: credential.response.getTransports ? credential.response.getTransports() : []
                    }
                })
            });
            
            const result = await registerResp.json();
            
            if (result.success) {
                showToast('Ключ "' + finalDeviceName + '" успешно добавлен!', 'success');
                setTimeout(() => location.reload(), 1500);
            } else {
                throw new Error(result.message || 'Ошибка регистрации');
            }
        } catch (err) {
            console.error('Registration error:', err);
            
            let errorMsg = err.message;
            if (err.name === 'NotAllowedError') {
                errorMsg = 'Операция отменена или биометрия не настроена';
            } else if (err.name === 'SecurityError') {
                errorMsg = 'Ошибка безопасности';
            } else if (err.name === 'NotSupportedError') {
                errorMsg = 'Ваш браузер не поддерживает WebAuthn';
            }
            
            showToast(errorMsg, 'error');
        }
    };
    
    // Сохраняем обработчик и добавляем событие
    registerBtn._webauthnHandler = handler;
    registerBtn.addEventListener('click', handler);
}

// Вспомогательные функции
function arrayBufferToBase64Url(buffer) {
    const bytes = new Uint8Array(buffer);
    let binary = '';
    for (let i = 0; i < bytes.byteLength; i++) {
        binary += String.fromCharCode(bytes[i]);
    }
    return btoa(binary).replace(/\+/g, '-').replace(/\//g, '_').replace(/=+$/, '');
}

// Функция показа уведомлений
function showToast(message, type = 'info') {
    const colors = {
        success: 'bg-emerald-500',
        error: 'bg-red-500',
        info: 'bg-blue-500'
    };
    
    const toast = document.createElement('div');
    toast.className = `fixed bottom-4 right-4 ${colors[type]} text-white px-4 py-3 rounded-lg shadow-lg z-50 animate-slide-up max-w-md text-sm whitespace-pre-line`;
    toast.textContent = message;
    document.body.appendChild(toast);
    
    setTimeout(() => {
        toast.classList.add('animate-fade-out');
        setTimeout(() => toast.remove(), 300);
    }, 4000);
}

